def known_galaxy_datatypes(extra_file: Path | None = None) -> set[str]:
    known = set(DEFAULT_KNOWN_GALAXY_DATATYPES)
    if extra_file and extra_file.exists():
        for line in extra_file.read_text(encoding="utf-8").splitlines():
            value = line.strip()
            if value and not value.startswith("#"):
                known.add(value)
    return known
